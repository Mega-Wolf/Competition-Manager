package other;
public class Player {
	
	/* Variables */
	private String name;
	private int number;
	
	/* Constructor */
	public Player(String name, int number) {
		this.name = name;
		this.number = number;
	}
	
	/* Setter */
	
	/* Getter */
	public String getName() {
		return name;
	}

	public int getNumber() {
		return number;
	}
}